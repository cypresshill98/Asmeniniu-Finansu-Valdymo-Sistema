    private void MenuOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenuOpenActionPerformed
    
        JFileChooser langas = new JFileChooser();
        int rez = langas.showOpenDialog(null);
        if(rez == JFileChooser.APPROVE_OPTION){
        File failas1 = langas.getSelectedFile();
        //System.out.println(failas1.getName());
        try {
                        Scanner failas = new Scanner(failas1);
                        while (failas.hasNext()) {
                            String eilute = failas.nextLine();
                            String[] dalys = eilute.split(";");
                            if (dalys.length == 2) {
                                String pavad = dalys[0];
                                String apras = dalys[1];
                                afv.pridetiKategorija(pavad, apras);
                            }
                            String[] dalys1 = eilute.split(";");
                            if (dalys1.length == 6) {
                                double sum = Double.parseDouble(dalys1[0]);
                                String katt = dalys1[1];
                                String apr = dalys1[2];
                                String kome = dalys1[3];
                                Date date1 = toDate(dalys1[4]);
                                String cekionr = dalys1[5];
                                afv.pridetiIslaida(sum, date1, apr, katt, kome, cekionr);
                            }
                            String[] dalys2 = eilute.split(";");
                            if (dalys2.length == 5) {
                                double sum = Double.parseDouble(dalys2[0]);
                                String katp = dalys2[1];
                                String apr = dalys2[2];
                                String kome = dalys2[3];
                                Date date1 = toDate(dalys2[4]);
                                afv.pridetiPajama(sum, date1, apr, katp, kome);
                            }
                        }
                        failas.close();
                    }catch (Exception e) {
                        e.printStackTrace();
                        JOptionPane.showMessageDialog(this, "Klaida nuskaitant faila","Klaida",JOptionPane.ERROR_MESSAGE);
                    }
    }
        katSar.atnaujintiKategorijuLentele();
        atnaujintiPagrLentele();
    }//GEN-LAST:event_MenuOpenActionPerformed

