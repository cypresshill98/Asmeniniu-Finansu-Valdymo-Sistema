    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
     istrinti_button.setEnabled(true);
     redg_button.setEnabled(true);
        try{
     DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
     int selectedRowIndex = jTable1.getSelectedRow();
     tableKategorija = model.getValueAt(selectedRowIndex, 0).toString();
     tablePavadinimas = model.getValueAt(selectedRowIndex, 1).toString();
     tableTipas = model.getValueAt(selectedRowIndex, 2).toString();
     tableData = model.getValueAt(selectedRowIndex, 3).toString();
     tableSuma = model.getValueAt(selectedRowIndex, 4).toString();
     }catch(Exception ex){
         istrinti_button.setEnabled(false);
         redg_button.setEnabled(false);
     }
    }//GEN-LAST:event_jTable1MouseClicked

