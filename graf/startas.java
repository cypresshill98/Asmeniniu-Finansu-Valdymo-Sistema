    public void atnaujintiPagrLentele(){
        ArrayList<Islaidos> isl = afv.gautiIslaiduSarasa();
        ArrayList<PiniguJudejimas> pin = afv.gautiPajamuSarasa();
        Collections.sort(pin);
        Collections.sort(isl);
        String[][] mas = new String[pin.size()+isl.size()][5];
        int i=0;
        for(PiniguJudejimas k:pin){
            if(dIki!=null && dNuo!=null){
            Date thisData = toDate(k.getData());
            if(thisData.before(dNuo)){
                continue;
            }else if(thisData.after(dIki)){
                continue;
            }}
            mas[i][0] = k.getKategorija();
            mas[i][1] = k.getAprasymas();
            mas[i][2] = "Pajamos";
            mas[i][3] = k.getData();
            mas[i][4] = Double.toString(k.getSuma());
            i++;
        }
        for(Islaidos is:isl){
            if(dIki!=null && dNuo!=null){
            Date thisData = toDate(is.getData());
            if(thisData.before(dNuo)){
                continue;
            }else if(thisData.after(dIki)){
                continue;
            }}
            mas[i][0] = is.getIslaidosKategorija();
            mas[i][1] = is.getIslaidosAprasymas();
            mas[i][2] = "Islaidos";
            mas[i][3] = is.getIslaidosData();
            mas[i][4] = Double.toString(is.getIslaiduSuma());
            i++;
        }
        
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                mas,
        new String [] {
                "Kategorija", "Pavadinimas", "Tipas", "Data", "Suma"
            }
        ));
    }
    
    
    private void MenuSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenuSaveActionPerformed
         ArrayList<Islaidos> isl = afv.gautiIslaiduSarasa();
                    ArrayList<PiniguJudejimas> pin = afv.gautiPajamuSarasa();
                    ArrayList<Kategorija> kat = afv.gautiKategorijuSarasa();
                    JFileChooser langas = new JFileChooser();
                    int rez = langas.showSaveDialog(null);
        if(rez == JFileChooser.APPROVE_OPTION){
                    File failas2 = langas.getSelectedFile();
                    try {
                        FileWriter faila = new FileWriter(failas2+".afv");
                        for (Kategorija k : kat) {
                            faila.write(k.getPavadinimas() + ";" + k.getAprasymas() + ";"+ "\r\n");
                        }
                        //faila.write("\r\n");
                        for (Islaidos is : isl) {
                            faila.write(is.getIslaiduSuma() + ";" + is.getIslaidosKategorija()
                                    + ";" + is.getIslaidosAprasymas() + ";" + is.getIslaidosKomentaras() + ";"
                                    + is.getIslaidosData() + ";"+ is.getCekioNr() + ";" + "\r\n");
                        }
                        //faila.write("\r\n");
                        for (PiniguJudejimas pj : pin) {
                            faila.write(pj.getSuma() + ";" + pj.getKategorija()
                                    + ";" + pj.getAprasymas() + ";" + pj.getKomentaras() + ";"
                                    + pj.getData() + ";" + "\r\n");
                        }
                        faila.close();
                    } catch (Exception e) {
                        e.printStackTrace();
                        JOptionPane.showMessageDialog(this, "Klaida issaugant i faila","Klaida",JOptionPane.ERROR_MESSAGE);
                    }
        }
    }//GEN-LAST:event_MenuSaveActionPerformed

