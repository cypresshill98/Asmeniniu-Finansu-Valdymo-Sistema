    private void data_nuoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_data_nuoKeyReleased
        String datNuo = data_nuo.getText();
        if((((4 == datNuo.indexOf("-")) && (7 == datNuo.indexOf("-",5)) && datNuo.length()==10) 
                && (datNuo.substring(5, 6).equals("0")||datNuo.substring(5, 6).equals("1")
                &&(datNuo.substring(8, 9).equals("0")||datNuo.substring(8, 9).equals("1")
                ||datNuo.substring(8, 9).equals("2")||datNuo.substring(8, 9).equals("3"))))||datNuo.equals("")){
            data_nuo.setBackground(Color.white);
            filtr_mygt.setEnabled(true);

        }else{
            data_nuo.setBackground(Color.red);
            filtr_mygt.setEnabled(false);
        }
    }//GEN-LAST:event_data_nuoKeyReleased

